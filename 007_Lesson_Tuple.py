#Tuple
#Tuple is a fast list that can not be modified

#Create a tuple
t = (1,2,3,4)
print(t)

#Access the elements
print(f'Index: {t[0]}')

#Slice the tuple
print(f'Slice: {t[2:]}')

#Look inside the tuple
print(f'Bool: {3 in t}')

#Assignment
(x, y, z) = (1, 2, 3)
print(x)
print(y)
print(z)

#Range functions
(x, y, z) = range(1,4)
print(x)
print(y)
print(z)
